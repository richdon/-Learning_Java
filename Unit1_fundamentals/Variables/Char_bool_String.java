package Unit1_fundamentals.Variables;

public class Char_bool_String {
    public static void main(String[] args) {
        boolean x = true;
        boolean y = false;
        System.out.println(y);
        System.out.printf("%s %s",x,y);

        char d = 'd';
        //hexadecimal
        char Z = '\u005A';
        System.out.println("\n"+d);
        System.out.println(Z);

        String r = "Russia is cold";

        System.out.println(r + " but that's ok");
    }
    //boolean


}
